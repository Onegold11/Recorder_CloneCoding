package com.clonecoding.recorder_clonecoding

/**
 * Recorder state enum class
 */
enum class State {

    /**
     * Before
     */
    BEFORE_RECORDING,

    /**
     * On recording
     */
    ON_RECORDING,

    /**
     * After
     */
    AFTER_RECORDING,

    /**
     * On playing
     */
    ON_PLAYING,
}